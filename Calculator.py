class Calculator:

    def add(self, a, b):
        return a + b


    def sub(self, a, b):
        return a - b


    def multiply(self, a, b):
        return a * b


    def division(self, a, b):
        return a / b


    def reminder(self, a, b):
        return a % b


    def pow(self, a, b):
        return a ** b